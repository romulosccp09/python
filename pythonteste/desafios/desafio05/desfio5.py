# Desafio 05 sucessor Antecessor de um número.

numero = int(input('Digite um número: '))

#Antecessor.
print('O antecessor de do número {}, vale -> {}!\n'.format(numero, numero - 1))

#Sucessor.
print('O sucessor do número {}, vale -> {}!'.format(numero, numero + 1))
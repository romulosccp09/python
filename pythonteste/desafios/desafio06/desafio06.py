# Desafio 06 mostrar dobro e raiz quadrada de um número.

numero = int(input('Digite um número: '))

# Dobro de um número.
print('O dobro do número {}, vale -> {}\n'.format(numero, numero * 2) )

# Raiz quadrada.
print('A raiz do numero {}, vale -> {}'.format(numero, numero ** (1/2)))
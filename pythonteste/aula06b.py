# Tipos primitivos

n = input("")
# Verifica se há possibilidade de o caractere ser Alfanumérico.
print(n.isalnum())
# Verifica se há possibilidade de o caractere ser uma letra.
print(n.isalpha())
# Verifica se há possibilidade de o caractere ser decimal.
print(n.isdecimal())
# Verifica se há possibilidade de o caractere ser
print(n.isdigit())
# Verifica se há possibilidade de o caractere ser
print(n.isidentifier())
# Verifica se há possibilidade de o caractere ser
print(n.isprintable)
# Verifica se há possibilidade de o caractere ser tipo primitivo.
print(type(n))
